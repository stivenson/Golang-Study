package main

import (
	"fmt"
)

var a, b, c int
var x, y, z bool
var aa, bb, cc int = 1, 2, 3
var xx, yy, zz = true, "hi", 100

func main() {
	var o, n string = "Aja", "y tu que"
	h, l, m := true, false, "Hello"

	fmt.Println(a, b, c, x, y, z)
	fmt.Println(aa, bb, cc, xx, yy, zz)
	fmt.Println(h, l, m)
	fmt.Println(o, n)
}
